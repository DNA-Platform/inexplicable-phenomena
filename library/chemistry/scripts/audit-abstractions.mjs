// Walk src/**/*.ts, extract every exported class with its members, and
// every exported free function. Output a markdown audit file.
// Usage: node scripts/audit-abstractions.mjs

import { readFileSync, writeFileSync, readdirSync, statSync } from 'fs';
import { join, relative } from 'path';
import { fileURLToPath } from 'url';

const here = fileURLToPath(new URL('.', import.meta.url));
const srcRoot = join(here, '..', 'src');
const outFile = join(here, '..', 'ABSTRACTIONS.md');

function walk(dir) {
    const out = [];
    for (const entry of readdirSync(dir)) {
        const path = join(dir, entry);
        const s = statSync(path);
        if (s.isDirectory()) out.push(...walk(path));
        else if (entry.endsWith('.ts') || entry.endsWith('.tsx')) out.push(path);
    }
    return out;
}

function parse(content, file) {
    const lines = content.split(/\r?\n/);
    const abstractions = [];
    let currentClass = null;
    let classDepth = 0;
    let braceDepth = 0;

    for (let i = 0; i < lines.length; i++) {
        const line = lines[i];
        const trimmed = line.trim();

        // Exported class
        const classMatch = trimmed.match(/^export\s+(?:abstract\s+)?class\s+(\$?\w+)(?:<[^>]*>)?(?:\s+extends\s+(\$?\w+))?/);
        if (classMatch && braceDepth === 0) {
            currentClass = {
                kind: 'class',
                name: classMatch[1],
                extends: classMatch[2] || null,
                members: [],
                line: i + 1,
            };
            abstractions.push(currentClass);
            classDepth = braceDepth;
        }

        // Exported free function
        const fnMatch = trimmed.match(/^export\s+function\s+(\$?\w+)\s*(?:<[^>]*>)?\s*\(/);
        if (fnMatch && braceDepth === 0) {
            abstractions.push({
                kind: 'function',
                name: fnMatch[1],
                line: i + 1,
            });
        }

        // Exported const at top level (arrow fns, singletons)
        const constMatch = trimmed.match(/^export\s+const\s+(\$?\w+)(?::\s*[^=]+)?\s*=\s*(.+)?$/);
        if (constMatch && braceDepth === 0) {
            const val = constMatch[2] ?? '';
            abstractions.push({
                kind: val.includes('new ') ? 'singleton' : (val.match(/^\(?[a-zA-Z_$]/) ? 'function' : 'const'),
                name: constMatch[1],
                line: i + 1,
            });
        }

        // Class member: method or field at the class depth + 1
        if (currentClass && braceDepth === classDepth + 1) {
            // Method: name(
            const methodMatch = trimmed.match(/^(?:(?:public|private|protected|static|readonly|async|get|set|\*)\s+)*(\[[^\]]+\]|\$?\w+)\s*(?:<[^>]+>)?\s*\(/);
            if (methodMatch) {
                const name = methodMatch[1];
                // Skip constructors of the form $Name(...) { - those are bond constructors
                // We'll include them though
                currentClass.members.push({
                    kind: trimmed.startsWith('get ') ? 'getter' :
                        trimmed.startsWith('set ') ? 'setter' :
                        name === 'constructor' ? 'constructor' : 'method',
                    name,
                });
            } else {
                // Field declaration: name: type = ..., name = ..., [symbol]: type
                const fieldMatch = trimmed.match(/^(?:(?:public|private|protected|readonly|static)\s+)*(\[[^\]]+\]|\$?\w+)\s*(?:\?)?(?::\s*[^=;]+)?(?:=\s*[^;]+)?;?\s*$/);
                if (fieldMatch && !trimmed.startsWith('//') && !trimmed.startsWith('*')) {
                    const name = fieldMatch[1];
                    if (name && !name.includes(' ')) {
                        currentClass.members.push({ kind: 'field', name });
                    }
                }
            }
        }

        // Track brace depth
        for (const c of line) {
            if (c === '{') braceDepth++;
            else if (c === '}') {
                braceDepth--;
                if (currentClass && braceDepth === classDepth) currentClass = null;
            }
        }
    }

    return abstractions;
}

const files = walk(srcRoot).sort();
let out = `# $Chemistry — Abstraction Audit\n\n`;
out += `Generated by \`scripts/audit-abstractions.mjs\`. Run again after code changes.\n\n`;
out += `Lists every exported class (with members), every exported function, every exported const/singleton, grouped by source file.\n\n`;
out += `Purpose: review the abstraction surface for cohesion. Each name here is a thing a consumer can import. Together they are the public framework.\n\n`;

for (const file of files) {
    const content = readFileSync(file, 'utf8');
    const abs = parse(content, file);
    if (abs.length === 0) continue;

    const relPath = relative(join(here, '..'), file).replaceAll('\\', '/');
    out += `\n## \`${relPath}\`\n\n`;

    for (const item of abs) {
        if (item.kind === 'class') {
            const ext = item.extends ? ` extends \`${item.extends}\`` : '';
            out += `### class \`${item.name}\`${ext}\n\n`;
            if (item.members.length === 0) {
                out += `  (no members detected)\n\n`;
            } else {
                for (const m of item.members) {
                    out += `  - ${m.kind}: \`${m.name}\`\n`;
                }
                out += `\n`;
            }
        } else {
            out += `- ${item.kind}: \`${item.name}\`\n`;
        }
    }
}

writeFileSync(outFile, out, 'utf8');
console.log(`Wrote ${outFile}`);
console.log(`Files scanned: ${files.length}`);
